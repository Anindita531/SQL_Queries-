-- Create employee table
CREATE TABLE emp (
    empno NUMBER(5) PRIMARY KEY,
    ename VARCHAR2(50),
    job VARCHAR2(50),
    mgr NUMBER(5),
    hiredate DATE,
    sal NUMBER(10,2),
    comm NUMBER(10,2),
    deptno NUMBER(3),
    CONSTRAINT fk_dept FOREIGN KEY (deptno) REFERENCES dept(deptno)
);

